# ─── Helpers ──────────────────────────────────────────────────────────────────

def _gen_key() -> str:
    """Generate a key in FORSAKEN-XXXX-XXXX-XXXX format."""
    chars = string.ascii_uppercase + string.digits
    segments = ["".join(random.choices(chars, k=4)) for _ in range(3)]
    return "FORSAKEN-" + "-".join(segments)


def _is_founder(member: discord.Member) -> bool:
    return any(r.id == FOUNDER_ROLE_ID for r in member.roles)


def _is_customer(member: discord.Member) -> bool:
    return any(r.id == CUSTOMER_ROLE_ID for r in member.roles)


def _fmt_ts(iso: str | None) -> str:
    if not iso:
        return "—"
    try:
        dt = datetime.fromisoformat(iso)
        return f"<t:{int(dt.timestamp())}:F>"
    except Exception:
        return iso


def _key_summary_lines(key: str, rec: dict, guild: discord.Guild | None = None) -> list[str]:
    """Return a list of lines describing a key record."""
    gen_user = "—"
    if guild and rec.get("generated_by"):
        m = guild.get_member(rec["generated_by"])
        gen_user = str(m) if m else f"<@{rec['generated_by']}>"

    claimed_user = "—"
    if guild and rec.get("claimed_by_discord"):
        m = guild.get_member(rec["claimed_by_discord"])
        claimed_user = str(m) if m else f"<@{rec['claimed_by_discord']}>"

    pw_display = f"||`{rec['password_hash'][:16]}…`||" if rec.get("password_hash") else "—"

    lines = [
        f"**Key:** `{key}`",
        f"**Duration:** {rec.get('duration', '—')}",
        f"**Status:** {'🔴 Disabled' if rec.get('disabled') else '🟢 Active'}",
        f"**Generated by:** {gen_user}  •  {_fmt_ts(rec.get('generated_at'))}",
        f"**Claimed by:** {claimed_user}",
        f"**Username:** `{rec.get('username') or '—'}`",
        f"**Password (hashed):** {pw_display}",
        f"**HWID:** `{rec.get('hwid') or '—'}`",
        f"**Registered:** {_fmt_ts(rec.get('registered_at'))}",
    ]
    return lines



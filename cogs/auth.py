class GenKeyModal(ui.Modal, title="Generate Key"):
    duration = ui.TextInput(
        label="Duration",
        placeholder="e.g. 30d, 7d, lifetime",
        style=discord.TextStyle.short,
        max_length=50,
    )
    note = ui.TextInput(
        label="Note (optional)",
        placeholder="e.g. for @username, promo, etc.",
        style=discord.TextStyle.short,
        max_length=200,
        required=False,
    )
    quantity = ui.TextInput(
        label="Quantity (1–25)",
        placeholder="1",
        style=discord.TextStyle.short,
        max_length=2,
        default="1",
    )

    async def on_submit(self, interaction: discord.Interaction):
        try:
            qty = max(1, min(25, int(self.quantity.value.strip())))
        except ValueError:
            qty = 1

        generated = []
        for _ in range(qty):
            key = _gen_key()
            # Avoid (extremely unlikely) collisions
            while key_exists(key):
                key = _gen_key()
            create_key(key, self.duration.value.strip(), interaction.user.id)
            generated.append(key)

        note_line = f"\n**Note:** {self.note.value.strip()}" if self.note.value.strip() else ""
        keys_block = "\n".join(generated)

        view = ui.LayoutView()
        view.add_item(ui.Container(
            ui.TextDisplay(f"## {'Key' if qty == 1 else f'{qty} Keys'} Generated"),
            ui.Separator(visible=True, spacing=discord.SeparatorSpacing.small),
            ui.TextDisplay(
                f"**Duration:** {self.duration.value.strip()}{note_line}\n"
                f"**Generated by:** {interaction.user.mention}\n\n"
                f"```\n{keys_block}\n```"
            ),
            ui.Separator(visible=True, spacing=discord.SeparatorSpacing.small),
            ui.TextDisplay("-# Forsaken Bot — Key Management"),
        ))
        await interaction.response.send_message(view=view, ephemeral=True)


class RegisterModal(ui.Modal, title="Register — Claim Your Key"):
    key_input = ui.TextInput(
        label="Your Key",
        placeholder="FORSAKEN-XXXX-XXXX-XXXX",
        style=discord.TextStyle.short,
        max_length=25,
    )
    username_input = ui.TextInput(
        label="Username",
        placeholder="Choose a username for the loader",
        style=discord.TextStyle.short,
        max_length=32,
    )
    password_input = ui.TextInput(
        label="Password",
        placeholder="Choose a password for the loader",
        style=discord.TextStyle.short,
        max_length=64,
    )

    async def on_submit(self, interaction: discord.Interaction):
        key      = self.key_input.value.strip().upper()
        username = self.username_input.value.strip()
        password = self.password_input.value.strip()

        if not key.startswith("FORSAKEN-"):
            await interaction.response.send_message(
                "Invalid key format. Keys look like `FORSAKEN-XXXX-XXXX-XXXX`.", ephemeral=True
            )
            return

        success, err = register_key(key, username, password, interaction.user.id)

        if not success:
            await interaction.response.send_message(f"Registration failed: **{err}**", ephemeral=True)
            return

        # Assign Customer role
        customer_role = interaction.guild.get_role(CUSTOMER_ROLE_ID)
        if customer_role and customer_role not in interaction.user.roles:
            try:
                await interaction.user.add_roles(customer_role, reason="Registered a Forsaken key")
            except discord.Forbidden:
                pass

        view = ui.LayoutView()
        view.add_item(ui.Container(
            ui.TextDisplay("## Registration Successful"),
            ui.Separator(visible=True, spacing=discord.SeparatorSpacing.small),
            ui.TextDisplay(
                f"Your key has been claimed and your account is ready.\n\n"
                f"**Username:** `{username}`\n"
                f"**Key:** `{key}`\n\n"
                f"Use your **username** and **password** to log in to the loader.\n"
                f"Your HWID will be bound on first launch.\n\n"
                f"-# Keep your credentials safe — do not share them."
            ),
            ui.Separator(visible=True, spacing=discord.SeparatorSpacing.small),
            ui.TextDisplay("-# Forsaken Bot — Registration"),
        ))
        await interaction.response.send_message(view=view, ephemeral=True)


